DROP TABLE INCOMING_TRANSFER_ORDER;-- IF EXISTS;
DROP TABLE PERSON;-- IF EXISTS; -- sample test table

CREATE TABLE INCOMING_TRANSFER_ORDER (
ORDER_ID BIGINT PRIMARY KEY,--GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
DESCRIPTION VARCHAR(500) NOT NULL,
DESTINATION_SERVER_ADDRESS VARCHAR(20) NOT NULL,
SCHEDULED_AT DATE,
STATUS VARCHAR(10) NOT NULL,
IS_RESUMABLE TINYINT NOT NULL, -- (0->FALSE,1->TRUE)
IS_PAUSED TINYINT,
LAST_TRANSFER_CHECKPOINT BIGINT
);

CREATE TABLE PERSON (
ID VARCHAR(10) NOT NULL PRIMARY KEY,
FIRST_NAME VARCHAR(15) NOT NULL,
LAST_NAME VARCHAR(15) NOT NULL,
JOINED_AT DATE NOT NULL,
DEPARTMENT VARCHAR(15),
JUST_IN TINYINT NULL
);
